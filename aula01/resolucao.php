

******* Problema
Para um pessoa com deficiencia fisica o poder de ir e vir é inviabilizado 
ao tentar se locomover de um ponto para outro usando trasporte publico 
ou aplicativo. Apesar de existir politicas publicas voltadas para pessoas 
com deficiencia fisica ainda existe uma lacuna de problemas porque nem 
sempre os elevadores dos onibus estão funcionando. Ademais, raramente os 
onibus passam nos horarios que as pessoas com deficiencia fisica precisa 
fazer o embargue. Outra alternativa, é o trasporte por apliativo que tambem 
é inviabilizado por causa dos motoristas que recusam as corridas ao perceber 
que o passageiro esta usando cadeira de rodas alem de na maioria das vezes 
os carros não tem espaço para carregar a cadeira de rodas.


******* Solução
A proposta consiste na criação de uma extensão integrada aos aplicativos Uber, 
99 e outros, introduzindo uma categoria exclusiva denominada "Roundder" 
voltada para corridas destinadas às pessoas com deficiência (PCD). 
Essas categoria direciona os veículo mais espaçosos que permita por 
objetos dentro dos veículos como cadeiras de rodas, entre outros além 
de proporcionar o conforto e segurança dos utilizadores da Roundder. 
O objetivo primário é incentivar os motoristas, e proporcionar uma 
alternativa mais acessível e inclusiva para esse público.

